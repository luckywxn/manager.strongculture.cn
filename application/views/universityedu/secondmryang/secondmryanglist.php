    <br><br>各位同学：
    <br><br>　　大家下午好！
    <br><br>　　首先我代表全校教师，热烈欢迎你们来到我校就读！我们全体教师已经准备好，要用几年的努力，把你们托举上新的高度，让你们踏上比我们更为绚丽多彩的锦绣前程！
    <br><br>　　但是要达到这一目标，光靠我们教师的努力是不够的，还必须获得你们的全力配合，在此我想对大家提出几点建议：
    <br><br>　　1．	要把中学的被动学习改为主动学习，课后要多多思考知识里蕴含的机理以及如何将各类知识融会贯通；大学是多学科多人才汇集的地方，要主动选择一些其他专业或学院去旁听，力争触类旁通。例如，三年前一位考上同济大学研究生的同学，在本科一年级时就开始向着考研究生目标努力，凡是学的课程与考研有关，就自觉提高对自己的要求，向同济门槛进军，结果一举成功。
    <br><br>　　2．	要在学习中变盲从为质疑，千万不要把书本和老师看作是百分之百的正确，因为没有人能保证不犯错误，更何况社会的发展决定了你们将超越前人，你们必须在走向社会之前更好地锤炼自己的思辨能力。例如，一位被宝钢集团录用的同学，在校读书时4年期间一直在家自学实际应用，没有停留在老师的讲解层面，前几天他们单位的领导来学校时告诉我今年这位同学加工资的幅度在同事中最高，大家都很羡慕他。
    <br><br>　　3．	要尽早谋划自己未来的职业生涯，由此制定出大学期间的学习规划，例如学习重点、社会实践、自学科目等。学校为此开设了一个专业导师工作室，汇集了10个专业的老师给大家出谋划策，请大家在国庆节后关注校园网上公告栏的通知；拿上学期来说，有的同学对所学专业始终难以入门，我们的这些老师就帮他分析他的擅长是否与所学专业吻合，是否需要调整学习方法，是否需要辅修一些其他课程，让他尽快寻找到适合自己的成才之路。在这里请大家记一下我们专业导师工作室的邮箱：zydsgzs@sspu.cn，请大家把自己的困惑和问题发邮件给我们，我们一定会帮助你们。
    <br><br>　　4．	要尽可能参加各类与学业相关的课外活动，使自己所学的知识尽快和实际应用结合起来，提升自己在交流、归纳、检索、设计、实施、总结、演讲等方面的能力。对此，学校为大家准备了很多大学生课外科技与创新活动，请大家关注校园网实验实训管理处网页上的相关信息；例如，今年5月份，我们学校的一组同学在市级科技竞赛中一举击败了上海所有高校的对手，夺得头筹，要知道当时复旦、交大、同济、华东理工、东华等高校的参赛选手中既有2个月后就将毕业的博士生，也有2年级的研究生，而我们的参赛选手只是本科3年级，大家想想，这是一种什么样的感觉。
    <br><br>　　5．	要尽快完善和巩固自己的文明礼仪，以便更好地学习与成长。例如，早上第一节课时不在教室吃早餐，上课时不迟到不睡觉不穿拖鞋不玩手机，按时认真完成各类学业任务，对待错误要做到“尽量不犯、尽早发现、尽快纠正”，兢兢业业、踏实做事、诚实做人。这方面有的同学做得很好，比如我们的一位被外企录用的同学，工作几年后企业对他的评价是：“诚实、勤奋、努力、专注——最可信任”。
    <br><br>　　以上是我对大家提的5点建议，希望你们认真思考，努力向上。
    <br><br>　　同学们，今天你们是我们的学生，明天你们将成为我们引以为豪的校友，让我们携起手来，为了你们美好的明天而共同奋斗吧！
    <br><br>　　谢谢大家！
